    ; void init_vga()
    ; Initialize VGA mode (0x13)
    init_vga:
        pushad

        ; Set mode
        mov esi, mode0x13
        call set_regs

        ; Set up palette
        mov esi, palette
        call set_palette

        ; Clear screen
        mov edi, 0xa0000
        mov ax, 0x0000
        mov ecx, 0x20000
        rep stosw

        popad
        ret

    ; Set VGA mode
    set_regs:
        cli
        mov dx, 0x3C2
        lodsb
        out dx, al

        mov dx, 0x3DA
        lodsb
        out dx, al

        xor ecx, ecx
        mov dx, 0x3C4
    .l1:
        lodsb
        xchg al, ah
        mov al, cl
        out dx, ax
        inc ecx
        cmp cl, 4
        jbe .l1

        mov dx, 0x3D4
        mov ax, 0x0E11
        out dx, ax

        xor ecx, ecx
        mov dx,  0x3D4
    .l2:
        lodsb
        xchg al, ah
        mov al, cl
        out dx, ax
        inc ecx
        cmp cl, 0x18
        jbe .l2

        xor ecx, ecx
        mov dx, 0x3cE
    .l3:
        lodsb
        xchg al, ah
        mov al, cl
        out dx, ax
        inc ecx
        cmp cl, 8
        jbe .l3

        mov dx, 0x3DA
        in al, dx

        xor ecx, ecx
        mov dx, 0x3C0
    .l4:
        in ax, dx
        mov al, cl
        out dx, al
        lodsb
        out dx, al
        inc ecx
        cmp cl, 0x14
        jbe .l4

        mov al, 0x20
        out dx, al

        sti
        ret

    ; void set_palette()
    ; Set palette colors
    set_palette:
        push ax
        push cx
        push dx

        xor cx, cx
    .next_color:
        mov dx, 0x03C8
        mov al, cl
        out dx, al
        inc dx
        mov al, byte [esi]
        out dx, al
        inc esi
        mov al, byte [esi]
        out dx, al
        inc esi
        mov al, byte [esi]
        out dx, al
        inc esi

        inc cx
        cmp cx, 256
        jl .next_color

        pop dx
        pop cx
        pop ax
        ret

